int glitch(int color)
{
    int r;
    int g;
    int b;

    b = color & 0x0000FF;       // B
    g = color >> 8 & 0x0000FF;  // G
    r = color >> 16 & 0x0000FF; // R
    color = 0;

    if (r >= 0 && r <= 255 && g >= 0 && g <= 255 && b >= 0 && b <= 255)
    {
        r = (r == 0 ? 255 : r - 1);
        color += r << 16;
        g = (g == 0 ? 255 : g - 1);
        color += g << 8;
        b = (b == 0 ? 255 : b - 1);
        color += b;
    }
    return (color);
}